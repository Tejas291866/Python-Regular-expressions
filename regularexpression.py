import re

def matchOccurrences(text):
    pattern1 = r'\b[Aa]b*\b'  # Zero or more b's
    pattern2 = r'\b[Aa]b?\b'  # Zero or one b
    pattern3 = r'\b[Aa]b+\b'  # One or more b's
    pattern4 = r'\b[Aa]bbbbc\b'  # Four consecutive b's followed by a "c"

    occurrences1 = re.findall(pattern1, text)
    occurrences2 = re.findall(pattern2, text)
    occurrences3 = re.findall(pattern3, text)
    occurrences4 = re.findall(pattern4, text)

    return occurrences1, occurrences2, occurrences3, occurrences4


# Example usage
text = "Ab abb Ac Abbbbc Ad AB Abbbbcb"

occurrences1, occurrences2, occurrences3, occurrences4 = matchOccurrences(text)

print("Occurrences of zero or more b's:", occurrences1)
print("Occurrences of zero or one b:", occurrences2)
print("Occurrences of one or more b's:", occurrences3)
print("Occurrences of four consecutive b's followed by a 'c':", occurrences4)

