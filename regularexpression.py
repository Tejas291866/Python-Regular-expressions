def searchAndReplace(pattern, replacement, string):
    locations = []
    start = 0
    while True:
        index = string.find(pattern, start)
        if index == -1:
            break
        locations.append(index)
        start = index + len(pattern)
    replaced_string = string.replace(pattern, replacement)
    return locations, replaced_string



string = "the world is full of hypocrites"
